public class SumofSqure {
    public static void main(String[] args) {
       int sum=0, n=123;  
       for(;n!=0; n/=10){
        int rem=n%10;
        int sq=rem*rem;
        sum+=sq;
       }
       System.out.print("Sum of Square: " +sum);
    }
}


