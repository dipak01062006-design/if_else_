import java.util.Scanner;
public class Schlorship {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.print("Enter percentage:");
        double per=sc.nextDouble();
        System.out.print("Enter family income:");
        double income=sc.nextDouble();
        if(per>=80 && income<300000){
            System.out.println("Full Schlorship");
        }
     else if(per>=70 && income<500000){
            System.out.println("Partical Schlorship");
        }
        else{
            System.out.println("No Eligible");
        }

    }
}



