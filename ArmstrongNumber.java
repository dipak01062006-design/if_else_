public class ArmstrongNumber {
    public static void main(String[] args) {
        int sum=0, n=1234;
        for(;n!=0; n/=10){
            int rem=n%10;
             sum+=rem;
        }
        if (sum==n) {
            System.out.print("Armstrong number");
        }
        else{
            System.out.print(" not armstrong number");
        }
    }
}




