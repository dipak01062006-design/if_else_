public class ReverseDigits {
    public static void main(String[] args) {
        int rev=0, n=1234;
        for(;n!=0 ;){
            int rem=n%10;
            rev=(rev*10)+rem;
            n/=10;
        }
        System.out.print("ReverseDigits: " +rev );
    }
}



