import java.util.Scanner;
public class Voting {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.print("Enter ages:");
        int age=sc.nextInt();
        if(age>=18){
            System.out.println("Eligible vote");
        }
        else{
             System.out.println(" Not Eligible vote");
        }
        }
}



