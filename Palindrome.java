public class Palindrome {
    public static void main(String[] args) {
        int rev=0, n=1234, temp=n;
        for(;temp!=0 ;){
            int rem=temp%10;
            rev=(rev*10)+rem;
            temp/=10;
        }
        if(n==rev)
        System.out.println("palindrome"); 
    else
        System.out.println("Not palindrome");
    }
}



